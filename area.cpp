#include <iostream>
using namespace std;
int main(){
    /// DECLARACION DE VARIABLES
    double a, x, y, z;
    /// LEER VALORES DE ENTRADA
    cin>>a;
    /// CALCULAR EL VALOR DE X
    x=3*a+15;
    y=(x+3)/(x+1);
    z=(5*x+7*y)/(a*x*y);
    cout<<z<<endl;
    cout<<"ejemplo ramas"<< endl;
    cout<<"ESTO SE HIZO EN LA RAMA 2"<< endl;
    return 0;
}
